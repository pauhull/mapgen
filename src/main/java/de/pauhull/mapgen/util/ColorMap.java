    public static double colorDistance(Color color1, Color color2) {
        long rMean = ((long) color1.getRed() + color2.getRed()) / 2;
        long r = (long) color1.getRed() - color2.getRed();
        long g = (long) color1.getGreen() - color2.getGreen();
        long b = (long) color1.getBlue() - color2.getBlue();
        return Math.sqrt((((512 + rMean) * r * r) >> 8) + 4 * g * g + (((767 - rMean) * b * b) >> 8));
    }

